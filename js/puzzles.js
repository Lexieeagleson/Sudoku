    /**
     * Get a random puzzle for the specified difficulty
     * @param {string} difficulty - 'easy', 'medium', 'hard', or 'superhard'
     * @returns {Object} Puzzle object with puzzle and solution arrays
     */
    getRandomPuzzle(difficulty) {
        const puzzles = this[difficulty];
        if (!puzzles || puzzles.length === 0) {
            console.error(`No puzzles available for difficulty: ${difficulty}`);
            return this.easy[0];
        }
        const randomIndex = Math.floor(Math.random() * puzzles.length);
        return puzzles[randomIndex];
    },

    /**
     * Deep clone a puzzle to avoid mutation
     * @param {Array} puzzle - 2D array representing puzzle
     * @returns {Array} Cloned 2D array
     */
    clonePuzzle(puzzle) {
        return puzzle.map(row => [...row]);
    }
